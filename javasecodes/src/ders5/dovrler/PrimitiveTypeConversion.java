package ders5.dovrler;

public class PrimitiveTypeConversion {
 public static void main(String[] args) {
	short s=69;
	byte b=(byte)s;
	System.out.println(b);
	short a=589;
	byte a2=(byte)a;
	System.out.println(a2);
	long l=458;
	int l2=(int)l;
	System.out.println(l2);
	long k=52523635483L;
	int k2=(int)k;
	System.out.println(k2);
	
}
}
