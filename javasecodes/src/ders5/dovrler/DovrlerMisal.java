package ders5.dovrler;

public class DovrlerMisal {
	public static void main(String[] args) {
		System.out.println("1-den 100-e qeder olan cut ededler");
		for (int a = 0; a <= 100; a += 2) {
			System.out.println(a);

		}
		System.out.println("15 ve 40 araliginda olan ededler");
		for (int i = 15; i <= 40; i++) {
			System.out.println(i);

		}
		System.out.println("20 ve 50 araliginda olan tek ededler");
		for (int b = 21; b <= 50; b += 2) {
			System.out.println(b);
		}
	}
}
